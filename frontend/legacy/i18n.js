  let _lang = localStorage.getItem('baa_lang') || 'zh';

  function t(key, vars) {
    const dict = T[_lang] || T.zh;
    let s = (key in dict) ? dict[key] : ((key in T.zh) ? T.zh[key] : key);
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        s = s.replaceAll('{' + k + '}', v);
      }
    }
    return s;
  }

  function getLang() { return _lang; }

  function setLang(lang) {
    if (lang !== 'zh' && lang !== 'en') return;
    _lang = lang;
    localStorage.setItem('baa_lang', lang);
    applyI18n();
    document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
  }

  function applyI18n() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      el.innerHTML = t(el.dataset.i18nHtml);
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.placeholder = t(el.dataset.i18nPh);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = t(el.dataset.i18nTitle);
    });
    const btn = document.getElementById('lang-toggle');
    if (btn) btn.textContent = t('lang.toggle');
  }

  document.addEventListener('DOMContentLoaded', applyI18n);

  // Expose on BAA namespace for explicit imports; window.* aliases for legacy callers.
  const baa = globalThis.BAA || {};
  baa.i18n = Object.freeze({ t, getLang, setLang, applyI18n });
  globalThis.BAA = baa;
  window.t = t;
  window.getLang = getLang;
  window.setLang = setLang;
  window.applyI18n = applyI18n;
